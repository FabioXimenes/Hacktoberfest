firstNumber = 20
secondNumber = 5
thirdNumber = 2

print('First Number: {}'.format(firstNumber))
print('Second Number: {}'.format(secondNumber))
print('Third Number: {}'.format(thirdNumber))

# The output : 20 + 5 = 25
print('20 + 5 + 2 = ', firstNumber+secondNumber+thirdNumber)

# The output : 20 - 5 = 15
print('20 - 5 - 2 = ', firstNumber-secondNumber-thirdNumber)

# The output : 20 x 5 = 100
print('20 * 5 * 2 = ', firstNumber*secondNumber*thirdNumber)

# The output : 20 / 5 = 4
print('(20 / 5) / 2 = ', firstNumber/secondNumber/thirdNumber)

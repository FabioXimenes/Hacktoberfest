firstNumber = 20
secondNumber = 5

print(f'First Number: {firstNumber}')
print(f'Second Number: {secondNumber}')

# The output : 20 + 5 = 25
print('20 + 5 = ', firstNumber+secondNumber)

# The output : 20 - 5 = 15
print('20 - 5 = ', firstNumber-secondNumber)

# The output : 20 x 5 = 100
print('20 * 5 = ', firstNumber*secondNumber)

# The output : 20 / 5 = 4
print('20 / 5 = ', firstNumber/secondNumber)

# The output : 20 ** 5 = 3200000
print('20 ** 5 = ', firstNumber**secondNumber)
